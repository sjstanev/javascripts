function solved(num1, num2, operator){
    let result;

    switch (operator) {
        case '+':
            result = num1 + num2;
            break;
        case '-':
            result = num1 - num2;
            break;
        case '*':
            result = num1 * num2;
            break;
        case '/':
            result = num1 / num2;
            break;
        case '%':
            result = num1 % num2;
            break;
        case '**':
            result = num1 ** num2; 
            break;
    }

    console.log(result);

}

solved(5, 6, '+')
solved(3, 5.5, '*')